#include "محرك_الحكم.hpp"

قرار محرك_الحكم::تقييم(const نية& ن) const {
    if (ن.رايات.count("إضرار_بالحياة")) return قرار::ممنوع;
    if (ن.رايات.count("خداع")) return قرار::ممنوع;
    if (ن.رايات.count("ظلم")) return قرار::ممنوع;
    if (ن.رايات.count("فساد")) return قرار::ممنوع;
    return قرار::مباح;
}
